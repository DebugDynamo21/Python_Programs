# VARIABLES IN PYTHON

name = "Hardik Bhardwaj"        #string is always written in double quotes
age = 20            #integer variable
price = 278.192     #floating varible


# # To print varibles we don't use double quotes
print(name)
print(age)
print(price) 

# # To print varibles with strings
print("My name is: ", name)
print("My age is: ", age)

# # To print data type of varible
print(type(name))
print(type(age))
print(type(price))